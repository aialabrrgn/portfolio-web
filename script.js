// portadas de proyectos

// bamfer
const tituloContBamfer = document.querySelector(".titulo__proyecto-bamfer")
const imgContBamfer = document.querySelector(".proyectos__imgCont")

tituloContBamfer.addEventListener('mouseenter', () => {
    imgContBamfer.classList.add('proyectos__imgCont-bamfer');
});
tituloContBamfer.addEventListener('mouseleave', () => {
    imgContBamfer.classList.remove('proyectos__imgCont-bamfer');
});

